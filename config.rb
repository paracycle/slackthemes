helpers do
  def themes
    data.themes.sort_by { |theme| theme['name'] }.map do |theme|
        OpenStruct.new(theme.merge(
            slug: theme['name'].parameterize.underscore,
            digest: Digest::MD5.new.hexdigest(theme['colors'])
        ))
    end
  end
end
